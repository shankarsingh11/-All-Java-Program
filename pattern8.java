public class pattern8 {
    public static void main(String[] args) {
        // NUMBER PYRAMID
        int n=5;
        //int number =0;
        for(int i=1;i<=n;i++){
           
            for(int j=1;j<=i;j++){

                System.out.print(" "+i);
                 
            }

            System.out.println();
        }

    }
}
